public class Tipos {
    public static void main(String[] args) {
        // Enteros
        byte number1 = 1;
        short number2 = 2;
        int number3 = 3;
        long number4 = 4l;

        // Punto flotante
        float decimal1 = 4.9f;
        double decimal2 = 4.99d;

        //Caracter
        char caracter1 = 'b';

        //Booleanos
        boolean verdadero = true;
        boolean falso = false;

        //Cadenas texto
        String nombre = "Joe";
        String apellido = "Doe";

        //Tipos envoltorio
        Integer numero = null;
        Long numero2 = 2L;
    }
}
